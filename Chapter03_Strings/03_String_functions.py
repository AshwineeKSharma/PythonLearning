# String functions are the inbuilt functions available in python programming to make programming easier.  


check="This is just to check string function in python programming and just to check the result with it."

#  String Functions

print(len(check))           #used to check the length of the sting given
print(check.endswith("it."))   # used to find if the string given is ending with
print(check.count("p"))        # used to count the character/word in the string given
print(check.capitalize())      # used to capitalize the first letter of given string
print(check.find("python"))     # used to find the word/letter in the given string (first index occurance) 
print(check.replace("to check", "for checking")) # used to replace word/ character of the given string (all occurance)




