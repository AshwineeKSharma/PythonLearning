# Lists are the data container which can store values of any data types and it's value can be updated by the user.
# creating list using[]
lis=[1,2,3,4,5,6]
# printing the list items using print() function
print(lis)

# Changing content of list using  it's index value
lis[1]=98
print(lis[1])

# Displaying list value using negative index
print(lis[-5])

# list can include values of any data type.
anlist=[1,2,"mango","papaya",12.5,"pineapple", False,"Tom","Jerry","Mohan"]
print(anlist)


# Slicing The List
print(anlist[1:7])


