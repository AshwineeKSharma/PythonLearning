# Tuple methods are the inbulit methods(Functions) in python with it's own unique characteristics. 
tup=(1,3,5,7,9,1,0,3,1)

# count(1) is a tuple method used to find the number of 1 used in the assigned tuple.
c=tup.count(1)
print(c) #print(tup.count(1))  can also be used
print(tup.count(1))

#index(1) is a tuple method used to find the index of 1st value of 1.
ci=tup.index(1)
print(ci) #print(tup.index(1)) can also be used.
print(tup.index(1))


