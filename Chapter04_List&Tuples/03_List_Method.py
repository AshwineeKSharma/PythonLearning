# List Method are the inbuilt functions with various task assigned with them
Lis=[1,9,6,8,5,3,2,10]

# append(value) is the list method used to append(add) data at the end of the list
Lis.append(4)

# insert(index,value) is the list method used to insert value in specified index 
Lis.insert(3,7)

print(Lis)
# sort() is a list method used to sort integer data in ascending order
Lis.sort()

#Printing the sorted List.
print(Lis)

# reverse() is a list method used to sort integer data in reverse order
Lis.reverse()

#printing Reversed List.
print(Lis)

# pop(index number) is a list method used to delete value at given index

Lis.pop(2)
print(Lis)

# remove(value) is a list method used to delete the given value in the list

Lis.remove(2)
print(Lis)

